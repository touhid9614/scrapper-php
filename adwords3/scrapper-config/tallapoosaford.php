    function filter_tallapoosaford_field_price($price,$car_data, $spltd_data)
        {
            $prices = [];

            slecho('');

            if($price && numarifyPrice($price) > 0) {
                $prices[] = numarifyPrice($price);
                slecho("tallapoosaford Price: $price");
            }

            $sale_regex =  '/Sale Price.*class=\'value[^>]+>(?<price>\$[0-9,]+)/';
            $internet_regex =  '/Internet Price.*class=\'value[^>]+>(?<price>[^<]+)/';

            $matches = [];

            if(preg_match($sale_regex, $spltd_data, $matches) && numarifyPrice($matches['price']) > 0) {
                $prices[] = numarifyPrice($matches['price']);
                slecho("Regex sale: {$matches['price']}");
            }

            if(preg_match($internet_regex, $spltd_data, $matches) && numarifyPrice($matches['price']) > 0) {
                $prices[] = numarifyPrice($matches['price']);
                slecho("Regex internet: {$matches['price']}");
            }

            if(count($prices) > 0) {
                $price = butifyPrice(min($prices));
            }

            slecho("Sale Price: {$price}".'<br>');
            return $price;
        }